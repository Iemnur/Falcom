class ItcEntry:
    def __init__(self, offset, size):
        self.offset = offset
        self.size = size

ITC_ENTRY_COUNT     = 160
ITC_ENTRY_SIZE      = 8
SIZE_OF_MAGIC       = 4
SIZE_OF_SCALE_INFO  = 512 * 4

def main():
    prefix = sys.argv[1].rsplit('_', 1)[0]

    print('combine %s_XXXXXX.itp into %s.itc ...' % (prefix, prefix), end = '')

    index = 0
    itc = fileio.FileStream(prefix + '.itc', 'wb')
    itc.Position = SIZE_OF_MAGIC + ITC_ENTRY_COUNT * ITC_ENTRY_SIZE + SIZE_OF_SCALE_INFO

    entries = []
    offset = 0

    while True:
        name = '%s_%06d.itp' % (prefix, index)
        index += 1
        try:
            itp = fileio.FileStream(name, 'rb')
        except FileNotFoundError:
            break

        entry = ItcEntry(itc.Position, itp.Length)
        entries.append(entry)

        itc.Write(itp.Read())
        itp.Close()

    itc.Position = 0
    itc.Write(b'V101')
    for entry in entries:
        itc.WriteULong(entry.offset)
        itc.WriteULong(entry.size)

    itc.Position = SIZE_OF_MAGIC + ITC_ENTRY_COUNT * ITC_ENTRY_SIZE

    for info in ScaleInfo:
        itc.WriteULong(info)

if __name__ == '__main__':
    Try(main)
